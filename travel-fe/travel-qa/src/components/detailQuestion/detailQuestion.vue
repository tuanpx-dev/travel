<template>
  <div class="col-md-8 list-qa">
    <div class="home-header">
      <input type="search" placeholder="search"/>
    </div>
    <div class="home-menu">
      <button class="home-menu-page">Popular</button>
      <button class="home-menu-page new">New</button>
      <button class="home-menu-page new">Relation</button>
    </div>
    <div v-if="!loading" >
      <div class="home-list">
        <Question :question="questionDetail" :page="'detail-question'"/>

        <!-- <div>
          <div v-for="answer in answers" :key="answer.id">
            <Comment :answer="answer"/>
          </div>
        </div> -->
      </div>
    </div>
    <div v-else>
      <b-spinner class="m-5"></b-spinner>
    </div>
  </div>
</template>

<script>
import request from '../../../request/request'
import { URL } from '../../api/URL'
import Question from '../home/Question'
// import Comment from './Comment'

export default {
  name: 'DetailQuestion',

  components: {
    Question
    // Comment
  },

  data () {
    return {
      loading: true,
      questionDetail: {},
      answers: [],
      options: []
    }
  },

  created () {
    this.getDetailQuestion()
  },

  methods: {
    getDetailQuestion () {
      request({
        url: URL.DETAIL_QUESTION(this.$route.query.id),
        method: 'get'
      })
        .then(res => {
          this.questionDetail = res.data
          this.getListAnswer(this.questionDetail.id)
        })
        .catch((e) => {
          if (e.response.status === 401) {
            localStorage.setItem('user', null)
            this.$router.push({ path: '/login' })
          }
        })
    },

    getListAnswer (id) {
      this.loading = true

      request({
        url: URL.ANSWERS_QUESTION(id),
        method: 'get'
      })
        .then(res => {
          this.loading = false
          this.answers = res.data.content
        })
        .catch((e) => {
          if (e.response.status === 401) {
            localStorage.setItem('user', null)
            this.$router.push({ path: '/login' })
          }
        })
    }
  }
}
</script>

